package com.camerino.ids.fps.client;

public class ClsItinerarioVisual
{
    String id;

    String nome;

    String ordinato;

    String tappe;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getOrdinato() {
        return ordinato;
    }

    public void setOrdinato(String ordinato) {
        this.ordinato = ordinato;
    }

    public String getTappe() {
        return tappe;
    }

    public void setTappe(String tappe) {
        this.tappe = tappe;
    }
}
